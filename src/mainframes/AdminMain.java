    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // update the database
        DefaultTableModel table = (DefaultTableModel) librarianTable.getModel();
        int row = librarianTable.getSelectedRow();//return the index of the first row selected 
        
        String name = (String) table.getValueAt(row, 0);
        String gender = (String) table.getValueAt(row, 1);
        String address = (String) table.getValueAt(row, 2);
        int contact = Integer.parseInt( table.getValueAt(row, 3).toString());
        String shift = (String) table.getValueAt(row, 4);
        
        LibrarianForm show = new LibrarianForm();
        show.getLibrarianInfo(name,gender, address,contact, shift);
        show.setVisible(true);
        dispose();
    }//GEN-LAST:event_updateBtnActionPerformed

