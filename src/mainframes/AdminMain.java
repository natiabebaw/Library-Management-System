      public void select(){
         DefaultTableModel addTable = (DefaultTableModel) librarianTable.getModel();
         Statement state;
         AdminMain table = new AdminMain();
        try {
            state = Lms_Connection.lms_con.createStatement();

            ResultSet add = state.executeQuery("SELECT * FROM librarian");
            while (add.next()) {

                String name = add.getString("name");
                String gender = add.getString("gender");
                String address = add.getString("address");
                int contact = add.getInt("contact");
                String shift = add.getString("shift");
                addTable.addRow(new Object[] {name,gender,address,contact,shift});
            }
            
            state.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
}
     public void removeData(){
          
          DefaultTableModel addTable = (DefaultTableModel) librarianTable.getModel();
          while(addTable.getRowCount()>0){
              addTable.removeRow(0);
          }
     }
     
     
